    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          Conexion Con =new Conexion();
        Connection cn =Con.Conectado();
        String Nombre_P,Numero,Adress,Precio_Compra;
        String sql="";
        Nombre_P = Nombre_Proveedor.getText();
        Numero = Contacto.getText();
        Adress = Direccion.getText();
        
        
        sql="INSERT INTO proveedor(nombre_proveedor,direccion,Contacto) Values(?,?,?)";
        try{
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,Nombre_P);
            pst.setString(2,Adress);
            pst.setString(3,Numero);
            int n=pst.executeUpdate();
            Con.desconectar();

            if(n>0){
              Nombre_Proveedor.setText(null);
              Contacto.setText(null);
              Direccion.setText(null);
              
        JOptionPane.showMessageDialog(null,"Se han a√±adido nuevos Proveedores");
            }
        }catch(SQLException e){System.out.print(e);}
    }//GEN-LAST:event_jButton1ActionPerformed

